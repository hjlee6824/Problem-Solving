#include <bits/stdc++.h>
using namespace std;

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);

    int n;
    cin >> n;

    while (n--)
    {
        int k;
        cin >> k;
        
        for (int i = 0; i < k; ++i) cout << '=';

        cout << '\n';
    }

    return 0;
}